class APIGenerator:
    """
    يحول نتيجة الـ Parser إلى كود API بسيط (Node.js)
    """

    def __init__(self, parsed_data):
        self.data = parsed_data

    def generate(self):
        intent = self.data["intent"]
        path = self.data["path"]
        method = self.data["method"].lower()
        inputs = self.data["inputs"]

        code = ""
        code += "// Auto-generated by MadiLang\n"
        code += "import express from 'express';\n"
        code += "const app = express();\n"
        code += "app.use(express.json());\n\n"

        # route
        code += f"app.{method}('{path}', (req, res) => {{\n"

        # inputs
        code += "  const { " + ", ".join(inputs) + " } = req.body;\n"

        # validation
        code += "  if (!" + " || !".join(inputs) + ") {\n"
        code += "    return res.status(400).json({ error: 'Missing fields' });\n"
        code += "  }\n\n"

        code += f"  // Intent: {intent}\n"
        code += "  res.json({ success: true });\n"

        code += "});\n\n"

        code += "app.listen(3000, () => console.log('Server running'));\n"

        return code
